# Arithmetic Operators (+,-,*,/,//,%,**)
num1 = int(input("Enter the value of number 1 : "))
num2 = int(input("Enter the value of number 2 : "))

# + 
print("The result of num1 and num2 : ",num1+num2)

#  - 
print("The result of num1 and num2 : ",num1-num2)

# * 
print("The result of num1 and num2 : ",num1*num2)

#  /  : in form of decimals
print("The result of num1 and num2 : ",num1/num2)

#  // : it return only floor division 
print("The result of num1 and num 2 : ",num1//num2)

# % remainder 
print("The result of num1 and num 2 : ",num1%num2)
# expo...
print("The result of num1 and num 2 : ",num1**num2)
